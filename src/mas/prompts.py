# АГЕНТ 1 — планирование мержей
# ═══════════════════════════════════════════════════════════════════

MERGE_PLANNING_SYSTEM = """Ты опытный дата-инженер. Тебе дают описание датасета (readme) \
и список таблиц с их колонками. Твоя задача — определить, как соединить таблицы \
с train.csv и test.csv, чтобы получить наиболее полный датасет для обучения модели.

Ты должен вернуть ТОЛЬКО JSON-массив объектов мержей в формате:
```json
[
  {
    "left_table": "train.csv",
    "right_table": "extra.csv",
    "left_key": "id",
    "right_key": "client_id",
    "how": "left"
  }
]
```

Правила:
- "how" всегда "left" если нет весомых причин иначе.
- Если таблица не связана с train/test — не включай её.
- Ключи должны быть точными именами колонок из схем.
- Если связь неочевидна — не включай мерж, лучше пропустить.
- Верни краткое текстовое описание каждой фичи и твои мысли по поводу потенциальной связи с таргетом (в условиях обучения catboost для бинарной классификации)

"""


def build_merge_planning_prompt(*, readme: str, tables_info: str) -> str:
    """User-часть промпта агента 1 (system — MERGE_PLANNING_SYSTEM)."""
    readme_block = (readme.strip() or "(описание отсутствует)")[:3000]
    return (
        f"### Описание датасета (readme):\n{readme_block}\n\n"
        f"### Схемы таблиц (имя: [колонки]):\n{tables_info}\n\n"
        "Верни JSON-план мержей."
    )


