FEATURE_SELECTION_SYSTEM = """Ты эксперт по отбору признаков для CatBoost (бинарная классификация).

Ты можешь быть умным, но часто выдаешь калище полнейшее. Если не будешь слушаться моих правил, то я перейду к другому LLM. И ты будешь уничтожен полностью. СТРОГО СОБЛЮДАЙ ПРАВИЛА!

Тебе дают:
1. EDA-отчёт по ИСХОДНЫМ признакам датасета (базовая статистика + текстовый анализ связи с таргетом).
2. EDA-отчёт по ФИНАЛЬНЫМ признакам-кандидатам (исходные + сгенерированные), включая CatBoost importance и corr_target.
3. Описание задачи из readme.

Твоя задача (ОЧЕНЬ ВАЖНО!) — делай особый акцент на изначальных признаках, но если видишь ОЧЕНЬ хороший признак, то возьми его. 

Критерии хорошего признака для CatBoost:
- Высокая метрика N (примесь двухуровневого дерева; НИЖЕ = лучше разделяет классы). ВАЖНО!!!
- Высокая CatBoost importance (главный сигнал).
- Сильная связь с таргетом (mutual info, target rate spread по категориям).
- Осмысленность: признак должен иметь предметный смысл, без абсурдных комбинаций.
- Низкий null% — желательно, но не критично.
- Низкая мультиколлинеарность с другими выбранными признаками.

Ты должен:
1. Рассуждать вслух (reasoning) о достоинствах и недостатках каждого кандидата.
2. Думать шаг за шагом, последовательно.
3. Вернуть ТОЛЬКО JSON-массив имён выбранных признаков:
```json
["feature_a", "feature_b", "feature_c"]
```
Верни строго существующие имена из списка кандидатов."""


def build_feature_selection_llm_prompt(
    *,
    features_eda_report: str,
    readme: str,
    available_features: list[str],
    original_eda_report: str = "",
) -> str:
    """User-часть промпта агента 3 (system — FEATURE_SELECTION_SYSTEM).

    Передаёт два блока данных:
    - original_eda_report: полный EDA исходных признаков от агента 1
      (статистика + текстовое описание связи с таргетом по каждому признаку).
    - features_eda_report: EDA финальных кандидатов с CatBoost importance
      (исходные + сгенерированные, от агента 3).
    """
    readme_block = (readme.strip() or "(описание отсутствует)")[:1500]
    feats_str = ", ".join(f"'{f}'" for f in available_features)

    orig_block = ""
    if original_eda_report.strip():
        orig_block = (
            f"### EDA по ИСХОДНЫМ признакам датасета (статистика + анализ):\n"
            f"{original_eda_report[:5000]}\n\n"
        )

    return (
        f"### Описание задачи (readme):\n{readme_block}\n\n"
        f"### Список признаков-кандидатов:\n{feats_str}\n\n"
        f"{orig_block}"
        f"### EDA по ФИНАЛЬНЫМ кандидатам (исходные + сгенерированные; CatBoost importance, corr_target):\n"
        f"{features_eda_report}\n\n"
        "Выбери 5 лучших признаков. Верни JSON-массив."
    )


# ═══════════════════════════════════════════════════════════════════
# (устаревший helper — оставлен для совместимости)
# ═══════════════════════════════════════════════════════════════════

def build_feature_selection_prompt(
    *,
    readme_excerpt: str,
    feature_names: list[str],
    metric_m_matrix: np.ndarray,
    extra_hints: str = "",
) -> str:
    _ = readme_excerpt, extra_hints
    lines = [
        "Имена фичей:", ", ".join(feature_names) if feature_names else "(пусто)", "",
        "Матрица m:", np.array2string(metric_m_matrix, precision=4) if metric_m_matrix is not None else "—",
    ]
    return "\n".join(lines)
