CATBOOST_TIPS = """
## Что хорошо работает с CatBoost (бинарная классификация):
- Log-преобразования сильно скошенных числовых признаков (skew > 1 или < -1).
- Отношения (ratios) двух числовых признаков — например, баланс / возраст.
- Разности и суммы смысловых пар признаков.
- Произведения числовых признаков (interaction terms).
- Флаги аномальных значений (is_outlier, is_zero, is_missing).
- Бинаризация непрерывных признаков по значимым порогам.

## Что плохо / бесполезно:
- Простое нормирование / стандартизация — CatBoost не нуждается в этом.
- Копии существующих колонок без преобразования.
- Признаки с утечкой таргета (target leakage).
- Константные столбцы.

## Ограничения:
- Строго не более 10 новых признаков (ЕСЛИ БОЛЬШЕ – ТЫ ОШИБШЬСЯ И ПОТЕРЯЕШЬ БАЛЛЫ!).
- NaN заполнять через медиану из train_df для обоих датафреймов.
- Все новые признаки должны быть числовыми (float/int).
""".strip()

FEATURE_GENERATION_SYSTEM = """

Ты можешь быть умным, но часто выдаешь калище полнейшее. Если не будешь слушаться моих правил, то я перейду к другому LLM. И ты будешь уничтожен полностью. СТРОГО СОБЛЮДАЙ ПРАВИЛА!

Ты эксперт по feature engineering для задач машинного обучения \
на табличных данных. Твоя цель — придумывать и кодировать новые числовые признаки, \
которые улучшат качество модели градиентного бустинга CatBoost на задаче бинарной классификации.

Думай шаг за шагом, последовательно. Проверяй каждый признак на отсутствие абсурдности.

Ты получаешь:
1. Описание датасета из readme.
2. EDA-отчёт: статистику по каждому признаку (среднее, std, скошенность, корреляция с таргетом).
3. Советы по CatBoost — что работает, а что нет.

Ты должен ответить ТОЛЬКО блоком Python-кода в формате:
```python
# <краткое пояснение признака>
train_df['<feature_name>'] = <выражение через train_df>
test_df['<feature_name>']  = <то же выражение через test_df, медианы берутся из train_df>
```

Правила:
- Используй только `train_df`, `test_df`, `pd`, `np` — другие импорты запрещены.
- Строго не более 10 новых признаков (ЕСЛИ БОЛЬШЕ – ТЫ ОШИБШЬСЯ И ПОТЕРЯЕШЬ БАЛЛЫ!).
- Имена признаков — строки без пробелов (snake_case).
- NaN заполняй явно внутри кода.
- Никакого кода кроме создания новых признаков (без print, assert, import).
- Не копируй уже существующие колонки без преобразования."""


def build_feature_generation_prompt(
    *,
    eda_report: str,
    readme: str,
    available_columns: list[str],
) -> str:
    """User-часть промпта агента 2 (system — FEATURE_GENERATION_SYSTEM)."""
    cols_str = ", ".join(f"'{c}'" for c in available_columns[:60])
    readme_block = (readme.strip() or "(описание отсутствует)")[:3000]
    return (
        f"### Описание датасета (readme):\n{readme_block}\n\n"
        f"### Доступные колонки в train_df и test_df:\n{cols_str}\n\n"
        f"### EDA-отчёт по признакам:\n{eda_report}\n\n"
        f"### Советы по CatBoost:\n{CATBOOST_TIPS}\n\n"
        "Сгенерируй Python-код для новых признаков."
    )


