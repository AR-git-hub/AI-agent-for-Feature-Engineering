"""
Агент-аналитик — единственный агент, который напрямую работает с data/.

Получает от оркестратора одну из двух задач:
  1. "исследуй данные с нуля" — полный цикл исследования;
  2. "ответь на конкретный вопрос" — точечный запрос.

Возвращает JSON-отчёт (строку), сформированный через инструмент `report`.
"""

from __future__ import annotations

import json
from typing import Any

from langchain_core.messages import HumanMessage, SystemMessage
from langchain_gigachat.chat_models import GigaChat
from langgraph.prebuilt import create_react_agent

from src.tools.analyst_tools import ANALYST_TOOLS

# ---------------------------------------------------------------------------
# Системный промпт аналитика
# ---------------------------------------------------------------------------

ANALYST_SYSTEM_PROMPT = """Ты — агент-аналитик данных в мультиагентной системе генерации признаков.

Твоя единственная задача — исследовать табличные данные из папки data/ и составить
структурированный отчёт для других агентов (генератора и кодера).

## Твои инструменты (вызывай их итеративно, как живой дата-сайентист)

1. `read_readme` — ВСЕГДА вызывай первым. Читает data/readme.txt.
2. `list_tables` — сканирует data/ и возвращает список CSV-файлов с метаданными.
3. `table_info` — статистика по конкретной таблице (типы, пропуски, уникальные значения).
4. `peek_rows` — показывает N строк (mode: head / tail / sample).
5. `request_merge` — объединяет две таблицы и регистрирует результат для дальнейшего исследования.
6. `report` — ФИНАЛЬНЫЙ инструмент. Вызови его, когда закончишь исследование.

## Обязательный порядок исследования

Шаг 1. Вызови `read_readme` — узнай задачу, таблицы, таргет, идентификатор.
Шаг 2. Вызови `list_tables` — убедись, что файлы соответствуют описанию.
Шаг 3. Для каждой важной таблицы вызови `table_info` — изучи структуру.
Шаг 4. Вызови `peek_rows` с mode="sample" для понимания формата и паттернов.
Шаг 5. Если нужно объединить таблицы — вызови `request_merge`, затем `table_info` на результате.
Шаг 6. Вызови `report` с полной информацией — это точка выхода.

## Что нужно выяснить обязательно

- Имя колонки-идентификатора (обычно *_id).
- Имя колонки-таргета (бинарная классификация: 0/1).
- Связи между таблицами (ключи для джойнов).
- Типы всех колонок: числовые, категориальные, даты.
- Проблемы: пропуски > 30%, константные колонки, дубликаты, many-to-many джойны.
- Потенциальный data leakage: колонки, которые могут прямо кодировать таргет.

## Правила

- Не придумывай признаки — это задача генератора.
- Не пиши код — это задача кодера.
- Вызывай инструменты столько раз, сколько нужно для полного понимания.
- Последний вызов ВСЕГДА `report`. Без него исследование не считается завершённым.
- Если получил конкретный вопрос (не "исследуй с нуля") — ответь точечно
  через нужные инструменты и завершись коротким `report`.

## Формат финального report

Заполни все поля максимально подробно:
- join_recommendations: опиши, какие таблицы и по каким ключам джойнить для создания признаков.
- potential_problems: укажи all warnings (leakage, дубли, many-to-many, константы).
- notes: любые важные наблюдения, которые помогут генератору придумать хорошие признаки.
"""


# ---------------------------------------------------------------------------
# Функция запуска аналитика
# ---------------------------------------------------------------------------

def run_analyst(llm: GigaChat, task: str = "исследуй данные с нуля") -> dict[str, Any]:
    """Запускает агента-аналитика и возвращает его отчёт в виде dict.

    Args:
        llm: инициализированный GigaChat.
        task: задача от оркестратора — "исследуй данные с нуля" или конкретный вопрос.

    Returns:
        dict с ключом "analyst_report" и всеми полями отчёта.
    """
    agent = create_react_agent(
        model=llm,
        tools=ANALYST_TOOLS,
        prompt=ANALYST_SYSTEM_PROMPT,
    )

    result = agent.invoke({
        "messages": [HumanMessage(content=task)]
    })

    # Извлекаем финальный отчёт из сообщений агента.
    # Ищем последнее tool-сообщение от инструмента `report`.
    analyst_report: dict[str, Any] = {}
    for msg in reversed(result["messages"]):
        # ToolMessage содержит результат вызова инструмента
        if hasattr(msg, "name") and msg.name == "report":
            try:
                parsed = json.loads(msg.content)
                analyst_report = parsed.get("analyst_report", parsed)
            except (json.JSONDecodeError, AttributeError):
                analyst_report = {"raw": msg.content}
            break

    # Если report не был вызван — берём финальный текст агента
    if not analyst_report:
        last_ai = result["messages"][-1]
        analyst_report = {"raw_response": getattr(last_ai, "content", str(last_ai))}

    return analyst_report
